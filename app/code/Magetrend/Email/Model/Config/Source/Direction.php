namespace Magetrend\Email\Model\Config\Source;

class Direction implements \Magento\Framework\Option\ArrayInterface
{
    /**
     * Options getter
     *
     * @return array
     */
    public function toOptionArray()
    {
        return [
            ['value' => 'ltr', 'label' => __('Left to Right')],
            ['value' => 'rtl', 'label' => __('Right to Left')]
        ];
    }

    /**
     * Get options in "key-value" format
     *
     * @return array
     */
    public function toArray()
    {
        return [
            'ltr' => __('Left to Right'),
            'rtl' => __('Right to Left')
        ];
    }
}
